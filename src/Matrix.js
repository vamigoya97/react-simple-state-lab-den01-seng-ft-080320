import React, { Component } from 'react';
import Cell from './components/Cell'

export default class Matrix extends Component {
  
  genRow = (vals) => {
    var count = 0;
    return vals.map(val => <Cell key={count++} value={val} />)
  }
  
  genMatrix = () => {
    var count = 0;
    return this.props.values.map(rowVals => <div className="row" key={count++}>{this.genRow(rowVals)}</div>)
  }
  
  render() {
    return (
      <div id="matrix">
        {this.genMatrix()}
      </div>
    )
  }
  
}

